package com.javacode.day2;

public class Increment_Decrement_Operators {

	public static void main(String[] args) {
        // ++   is known as increment operators
		// -- is known as decrement operators  
		
		
		int i = 10; 
		i++; // post increment operators 
		++i; // pre increment operators 
		i--; // post decrement operators 
		--i; // pre decrement operators 
		System.out.println(i);                                                                                                                                        
		
		
	}

}
